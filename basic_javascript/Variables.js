// 변수 선언 : 'let' 키워드
let integerValue = 123;
let stringValue = "문자열 변수";
let booleanValue = true;

console.log(integerValue);
console.log(stringValue);
console.log(booleanValue);

// 상수 선언 : 'const' 키워드
const integerConstant = 123;
const stringConstant = "문자열 상수";
const booleanConstant = true;

console.log(integerConstant);
console.log(stringConstant);
console.log(booleanConstant);